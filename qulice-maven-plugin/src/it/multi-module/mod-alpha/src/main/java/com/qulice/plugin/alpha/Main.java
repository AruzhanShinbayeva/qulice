@SuppressWarnings("PMD.ProhibitPublicStaticMethods")
public final class Main {
    /**
     * Utility constructor.
     */
    private Main() {
        // do nothing
    }

    /**
     * Calculate square of a number.
     * @param num The number
     * @return The square
     */
    public static int square(final int num) {
        return num * num;
    }

}
